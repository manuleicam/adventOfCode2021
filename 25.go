type newPos struct {
	crabMoveSymb string
	oldX         int
	oldY         int
	newX         int
	newY         int
}

func moveBeach(beachMatrix [][]string) []newPos {

	NextCrabPos := make([]newPos, 0)

	for y, line := range beachMatrix {

		for x, col := range line {

			if col == ">" {

				crabPos, canMove := moveRight(beachMatrix, x, y)

				if canMove {
					NextCrabPos = append(NextCrabPos, crabPos)
				}

			}

			if col == "v" {

				crabPos, canMove := moveDown(beachMatrix, x, y)

				if canMove {
					NextCrabPos = append(NextCrabPos, crabPos)
				}

			}

		}

	}

	return NextCrabPos
}

func moveRight(beachMatrix [][]string, x, y int) (newPos, bool) {

	nCols := len(beachMatrix[0]) - 1
	//nRows := len(beachMatrix) - 1

	if x == nCols {
		if beachMatrix[y][0] == "." {
			return newPos{">", x, y, 0, y}, true
		}
	} else {
		if beachMatrix[y][x+1] == "." {
			return newPos{">", x, y, x + 1, y}, true
		}
	}

	return newPos{}, false

}

func moveDown(beachMatrix [][]string, x, y int) (newPos, bool) {

	nCols := len(beachMatrix[0]) - 1
	nRows := len(beachMatrix) - 1

	newY := y + 1
	if newY > nRows {
		newY = 0
	}

	xEsq, xDir := getNewX(x, nCols)

	if beachMatrix[newY][x] == "v" {

		return newPos{}, false

	} else if (beachMatrix[newY][x] == ">" && beachMatrix[newY][xDir] == ".") || (beachMatrix[newY][xEsq] != ">" && beachMatrix[newY][x] == ".") {

		return newPos{"v", x, y, x, newY}, true

	}

	//else if beachMatrix[newY][xEsq] != ">" && beachMatrix[newY][x] == "." {

	//	return newPos{"v", x, y, x, newY}, true

	//}

	//if beachMatrix[newY][xEsq] == ">" && beachMatrix[newY][x] == "." {
	//	return newPos{}, false
	//}

	return newPos{}, false

}

func getNewX(x, nCols int) (int, int) {

	x1 := x - 1
	x2 := x + 1

	if x1 < 0 {
		x1 = nCols
	}
	if x2 > nCols {
		x2 = 0
	}

	return x1, x2

}

